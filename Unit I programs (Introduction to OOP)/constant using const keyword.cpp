//Write a program to declare a constant using const keyword
#include<iostream>
using namespace std;
int main()
{
	const int a=100;
	const float b=20.50;
	const char c='&';
	const double d=5055.90;
	cout<<"\n Value of integer constant 	: "<<a;
	cout<<"\n Value of floating point constant : "<<b;
	cout<<"\n Value of character constant 	: "<<c;
	cout<<"\n Value of Double constant 		: "<<d;
	return 0;
}
